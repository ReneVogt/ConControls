namespace ConControls.Controls.Text
{
    /// <summary>
    /// The wrap modes for controls derived from <see cref="TextControl"/>.
    /// </summary>
    public enum WrapMode
    {
        /// <summary>
        /// No wrapping at all.
        /// </summary>
        NoWrap,
        /// <summary>
        /// Simply wrap lines disregarding words or whitespaces etc.
        /// </summary>
        SimpleWrap
    }
}
